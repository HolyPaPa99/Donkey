/**
 * Principles:
 *     1. Start with too much white space, and then remove white space until everything looks better
 *     2. Distinguish outside and inside spacing, usally  inside is smaller
 */
public enum Spacing {
    public static let zero: CGFloat = 0
    public static let veriySmall: CGFloat = 2
    public static let smaller: CGFloat = 4
    public static let small: CGFloat = 8
    public static let medium: CGFloat = 16
    public static let large: CGFloat = 24
    public static let larger: CGFloat = 32
    public static let huge: CGFloat = 64
}
